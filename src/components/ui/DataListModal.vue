<template>
  <FullScreenModal
    v-model="modelValue"
    :title="title"
    subtitle="Sistema de Control de Accesos del IRCCA"
    :icon="headerIcon"
    :header-color="headerColor"
    @close="handleClose"
  >
    <!-- B√∫squeda r√°pida -->
    <div class="search-container" v-if="data.length > 0">
        <v-text-field
          v-model="searchQuery"
          prepend-inner-icon="mdi-magnify"
          label="Buscar..."
          :placeholder="dataType === 'personas' ? 'Buscar por nombre, documento o √°rea...' : 'Buscar por tipo, matr√≠cula o conductor...'"
          variant="outlined"
          density="compact"
          clearable
          hide-details
        />
    </div>

    <!-- Contenido principal -->
    <div class="content-container">
        <!-- Lista de personas con Virtual Scroll -->
        <template v-if="dataType === 'personas'">
          <v-virtual-scroll
            :items="filteredData"
            :height="virtualScrollHeight"
            item-height="80"
          >
            <template v-slot:default="{ item }">
              <PersonaCard 
                :persona="item as PersonaModalData"
                @click="emit('item-click', item)"
              />
            </template>
          </v-virtual-scroll>
        </template>

        <!-- Lista de veh√≠culos con Virtual Scroll -->
        <template v-else-if="dataType === 'vehiculos'">
          <v-virtual-scroll
            :items="filteredData"
            :height="virtualScrollHeight"
            item-height="80"
          >
            <template v-slot:default="{ item }">
              <VehiculoCard :vehiculo="item as VehiculoModalData" />
            </template>
          </v-virtual-scroll>
        </template>

        <!-- Estado vac√≠o -->
        <EmptyState
          v-if="filteredData.length === 0"
          :title="displayEmptyTitle"
          :subtitle="displayEmptySubtitle"
          :icon="displayEmptyIcon"
        />
    </div>

    <!-- Footer con contador y bot√≥n -->
    <template #footer>
      <div class="footer-actions">
        <span class="counter-text">
          {{ searchQuery ? `${filteredData.length} de ${data.length}` : `Total: ${data.length}` }} {{ dataType === 'personas' ? 'personas' : 'veh√≠culos' }}
        </span>
        <button class="btn-secondary" @click="closeModal">
          <i class="mdi mdi-close"></i>
          Cerrar
        </button>
      </div>
    </template>
  </FullScreenModal>
</template>

<script setup lang="ts">
import { ref, computed } from 'vue'
import FullScreenModal from './FullScreenModal.vue'
import PersonaCard from './PersonaCard.vue'
import VehiculoCard from './VehiculoCard.vue'
import EmptyState from './EmptyState.vue'
import { useDataListLogic, type PersonaModalData, type VehiculoModalData } from '@/composables/useDataListLogic'

interface Props {
  modelValue: boolean
  title: string
  subtitle?: string
  headerIcon: string
  headerColor?: string // Color din√°mico del header
  data: unknown[] // Tipo seguro para datos gen√©ricos
  dataType: 'personas' | 'vehiculos' // Tipo de datos para filtrado
  emptyTitle?: string
  emptySubtitle?: string
  emptyIcon?: string
}

const props = withDefaults(defineProps<Props>(), {
  headerColor: 'primary', // Default color
  emptyTitle: 'No hay datos disponibles',
  emptySubtitle: 'Actualmente no hay informaci√≥n para mostrar',
  emptyIcon: 'mdi-folder-open'
})

const emit = defineEmits<{
  'update:modelValue': [value: boolean]
  'item-click': [item: unknown]
}>()

// Estado para b√∫squeda
const searchQuery = ref('')

// Computadas para manejar el v-model
const modelValue = computed({
  get: () => props.modelValue,
  set: (value: boolean) => emit('update:modelValue', value)
})

// Usar l√≥gica del composable
const { filterData } = useDataListLogic()

// Datos filtrados por b√∫squeda
const filteredData = computed(() => {
  return filterData(props.data, searchQuery.value, props.dataType)
})

// üéØ MENSAJES CONTEXTUALES DIN√ÅMICOS
// Detecta si estamos en b√∫squeda activa o estado vac√≠o por defecto
const isSearchActive = computed(() => {
  return searchQuery.value.trim().length > 0
})

const hasNoResults = computed(() => {
  return props.data.length > 0 && filteredData.value.length === 0 && isSearchActive.value
})

// T√≠tulo din√°mico del estado vac√≠o
const displayEmptyTitle = computed(() => {
  if (hasNoResults.value) {
    return 'Sin coincidencias'
  }
  return props.emptyTitle
})

// Subt√≠tulo din√°mico del estado vac√≠o
const displayEmptySubtitle = computed(() => {
  if (hasNoResults.value) {
    return `No se encontraron resultados para "${searchQuery.value}"`
  }
  return props.emptySubtitle
})

// √çcono din√°mico del estado vac√≠o
const displayEmptyIcon = computed(() => {
  if (hasNoResults.value) {
    return 'mdi-magnify-close'
  }
  return props.emptyIcon
})
// Altura del virtual scroll (calculada)
const virtualScrollHeight = computed(() => {
  const dataLength = filteredData.value.length
  if (dataLength === 0) return 0

  const itemHeight = 88 // 80px item + 8px margin
  const minHeight = itemHeight * 4 // M√≠nimo 4 items visibles (352px)
  const maxHeight = 600 // M√°ximo para tablets (aprox 6-7 items)

  // Asegurar que siempre se muestren al menos 4 items (si hay datos)
  const calculatedHeight = dataLength * itemHeight
  return Math.max(minHeight, Math.min(calculatedHeight, maxHeight))
})

function closeModal() {
  searchQuery.value = '' // Limpiar b√∫squeda al cerrar
  modelValue.value = false
}

function handleClose() {
  closeModal()
}
</script>

